                                    Nummulitidae, Talâat Adrhosse, Bassin de Tirinesse, Koudiat Achacha, Source de
                                    Ras Ma, Source de Chrafate, Brèche de Chrafate, Glissière d’Ametrasse, Jbel
                                    Chrafate Panoramic, Panorama de Jbel Lakraa, Mica des schistes de Targha, Gneiss
                                    de Stehat, Kinzigite de Beni Bouzra, Delta de Tihissase, Péridotite de Beni
                                    Bouzra) grâce à leur VS, PUE et/ou PUT supérieurs à 75 %.
