                                <p style="text-align: justify;" class="mb-15">La plus grande partie du futur
                                    Géoparc de Chefchaouen, et plus particulièrement le Parc National de
                                    Talassemtane, s’étend sur la dorsale calcaire qui est formée par les sols
                                    carbonatés du Trias-Jurassique et qui constitue les reliques d’une marge
                                    continentale qui formait la marge continentale passive de la branche méridionale
                                    d’un ancien océan qui existait au Jurassique-Crétacé à l’emplacement de
                                    l’actuelle Méditerranée (Océan Téthys) fermée depuis le Crétacé supérieur suite
                                    à la convergence de la Plaques africaines et eurasiennes mais dont les vestiges
                                    ont été soit incorporés dans les chaînes de montagnes qui font partie de la
                                    ceinture orogénique alpine, soit engloutis sous la Méditerranée. </p>
                                <p style="text-align: justify;" class="mb-15">Le futur Géoparc a fait l’objet de
                                    nombreuses études géologiques scientifiques, principalement stratigraphiques,
                                    paléontologiques, sédimentologiques et structurelles, qui ont contribué aux
                                    reconstitutions paléogéographiques et à la compréhension de l’évolution
                                    géologique alpine des montagnes du Rif depuis plus de 180 millions d’années et
                                    par conséquent de son homologue de la Bétique, puisque les deux chaînes
                                    partagent une histoire géologique commune. </p>
                                <p style="text-align: justify;" class="mb-15">
                                    De plus, et d’un autre point de vue, les sols calcaires sont le terrain des
                                    phénomènes de karstification et des paysages karstiques par excellence. De
                                    splendides structures karstiques, superficielles (dolines, ouvalas, poljés,
                                    etc.) et souterraines (grottes à spéléothèmes) qui ont attiré de grands
                                    scientifiques, spéléologues et amateurs professionnels de spéléologie,
                                    subsistent dans le futur Géoparc. </p>
                                <p style="text-align: justify;" class="mb-15">
                                    Le futur Géoparc est riche d’un patrimoine géologique qui intéresse la
                                    communauté scientifique et paysagère nationale et internationale (cascades et
                                    lac Akchour, canyons et gorges de la vallée de l’Oued Laou, pâturages perchés à
                                    haute altitude rappelant les plateaux tibétains, etc.) attirant des touristes de
                                    différentes nationalités. Le futur Géoparc accueillera des lieux où les amateurs
                                    de sports de montagne pourront pratiquer leur sport favori (alpinisme, escalade,
                                    etc.).
                                </p>





